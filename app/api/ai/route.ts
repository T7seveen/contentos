function buildContext() {
  const ideas = getIdeas();
  const trends = getTrends();
  const top = trends.sort((a, b) => b.growth - a.growth)[0];
  return {
    ideas,
    trendingHashtag: top?.hashtag ?? "#AItools",
    topTrendGrowth: top?.growth ?? 340,
  };
}

const KEYWORD_MAP: [string[], keyof typeof INSIGHTS][] = [
  [["просмотр", "view", "drop", "падают", "упали", "снизились"], "views"],
  [["идея", "idea", "вирусн", "тема", "viral", "niche", "ниша"], "ideas"],
  [["план", "plan", "расписание", "schedule", "неделя", "week"], "plan"],
];

function pickInsight(text: string): string {
  const lower = text.toLowerCase();
  for (const [keywords, key] of KEYWORD_MAP) {
    if (keywords.some((k) => lower.includes(k))) {
      const ctx = buildContext();
      return INSIGHTS[key](ctx);
    }
  }
  const ctx = buildContext();
  return `
**AI-анализ вашего аккаунта**

Я проанализировал ваши данные:
- **${ctx.ideas.length} идей** в базе, из которых 3 с AI Score 90+
- **Топ-тренд**: ${ctx.trendingHashtag} растёт на ${ctx.topTrendGrowth}% — ваша ниша пересекается на 78%
- **Контент-здоровье**: 74/100 — хорошо, но есть зоны роста

**Главные рекомендации:**
1. Увеличить частоту постинга до 10–12 постов/неделю
2. Переключиться на вопросительные хуки (они дают 2.7× больше completion rate)
3. Публиковать строго в 19:00–21:00 по часовому поясу аудитории

Что именно вас интересует? Могу провести детальный анализ по любому направлению.
`;
}

export async function POST(req: NextRequest) {
  const { message } = await req.json();
  const response = pickInsight(message ?? "");
  // Simulate AI delay
  return NextResponse.json({ response });
}
